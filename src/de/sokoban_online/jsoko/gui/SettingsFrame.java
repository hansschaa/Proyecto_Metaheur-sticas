    private void jButtonApplyLookAndFeelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonApplyLookAndFeelActionPerformed
        // TODO add your handling code here:
        LookAndFeelInfo[] info = UIManager.getInstalledLookAndFeels();
        for (LookAndFeelInfo lfInfo : info) {
            if (lfInfo.getName().equals(lookAndFeelName)) {
                System.out.println(lfInfo.getClassName());
                try {
                    UIManager.setLookAndFeel(lfInfo.getClassName());
                    Utilities.updateUI(); // update all swing windows
                    Settings.currentLookAndFeel = lfInfo.getClassName(); // save it to settings file
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(SettingsFrame.class.getName()).log(Level.SEVERE, null, ex);
                } catch (InstantiationException ex) {
                    Logger.getLogger(SettingsFrame.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IllegalAccessException ex) {
                    Logger.getLogger(SettingsFrame.class.getName()).log(Level.SEVERE, null, ex);
                } catch (UnsupportedLookAndFeelException ex) {
                    Logger.getLogger(SettingsFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
}//GEN-LAST:event_jButtonApplyLookAndFeelActionPerformed

