    private void UpdateSettingsFrame() {

        //languages combo box
        int noLanguages = 0;
        for (String l : Translator.getAvailableLanguageNames()) {
            noLanguages++;
        }
        String[] ArrayLanguages = new String[noLanguages];
        int i = 0;
        for (String l : Translator.getAvailableLanguageNames()) {
            ArrayLanguages[i] = l;
            i++;
        }
        comboBoxLanguages.setModel(new javax.swing.DefaultComboBoxModel(ArrayLanguages));

        //look and feel combo box
        int noLookAndFeel = 0;
        LookAndFeelInfo[] info = UIManager.getInstalledLookAndFeels();

        for (LookAndFeelInfo lfInfo : info) {
            noLookAndFeel++;
        }
        String[] ArrayLookAndFeel = new String[noLookAndFeel];
        noLookAndFeel = 0;
        for (LookAndFeelInfo lfInfo : info) {
            ArrayLookAndFeel[noLookAndFeel] = lfInfo.getName();
            noLookAndFeel++;
        }
        jComboBoxLookAndFeelSelection.setModel(new javax.swing.DefaultComboBoxModel(ArrayLookAndFeel));

        // update checkboxes
        jCheckBoxDetectSimpleDeadlocks.setText(Texts.getText("detectSimpleDeadlocks"));
        jCheckBoxDetectBipartiteDeadlocks.setText(Texts.getText("detectBipartiteDeadlocks"));
        jCheckBoxDetectCorralDeadlocks.setText(Texts.getText("detectCorralDeadlocks"));
        jCheckBoxDetectFreezeDeadlocks.setText(Texts.getText("detectFreezeDeadlocks"));
        jCheckBoxSinglestepundo.setText(Texts.getText("singlestepundo"));
        jCheckBoxReverseMoves.setText(Texts.getText("treat_reverse_moves_as_undo"));
        jCheckBoxSound.setText(Texts.getText("sound"));
        jCheckBoxOptimize_moves.setText(Texts.getText("optimize_moves"));
    }

    protected File browseFile() {
        JFileChooser push = new JFileChooser();
        int returnValue = push.showOpenDialog(null);
        push.setVisible(true);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File selectedFile = push.getSelectedFile();
            System.out.println(selectedFile);
            return selectedFile;
        } else {
            return null;
        }

    }

    protected Color pickColor(Color before) {

        Color after = JColorChooser.showDialog(rootPane, "Choose a Color", before);
        if (after == null) {
            return before;
        }
        return after;
    }

    private void previewFont() {
        resultName = fontNameChoice.getSelectedItem();
        String resultSizeName = fontSizeChoice.getSelectedItem();
        int resultSize = Integer.parseInt(resultSizeName);

        int attrs = Font.PLAIN;
        if (bold.isSelected()) {
            attrs = Font.BOLD;
        }
        if (italic.isSelected()) {
            attrs |= Font.ITALIC;
        }

        resultFont.setFont(new Font(resultName, attrs, resultSize));

    }

